<h1>Política de privacidad de PCtrinitat</h1>
<p>Esta Política de privacidad le informa sobre nuestras políticas con respecto a la recopilación, el uso y la divulgación de datos personales cuando interactúa con nuestro sitio web.

Usamos sus datos por varias razones, desde comprender cómo los usuarios interactúan con nuestro sitio web hasta publicar el contenido que lee en nuestro sitio web.

Estas son las principales razones por las que recopilamos y utilizamos datos sobre nuestros usuarios:

Para publicar contenido que sea relevante para nuestros usuarios y para mejorar su experiencia en nuestro sitio web.
Para llevar a cabo análisis de marketing y estadísticas agregadas.
Para enviar comunicaciones a nuestros usuarios cuando tengamos su permiso.
Para permitirnos mostrar publicidad en nuestros sitios.
Al utilizar nuestro sitio web, usted acepta la recopilación y el uso de información de acuerdo con esta política.</p>
<h2>Recopilación y uso de información </h2>
<p>Recopilamos varios tipos diferentes de información para varios propósitos para brindarle y mejorar nuestro Servicio.

No requerimos registro de usuario. Los usuarios son libres de visitar W3Docs y navegar por todas sus páginas. Sin embargo, mientras usa nuestro sitio web, podemos pedirle que nos proporcione cierta información de identificación personal que se puede usar para contactarlo o identificarlo ("Datos personales"). La información de identificación personal puede incluir, pero no se limita a:

Dirección de correo electrónico
Nombre y apellido
Cookies y datos de uso </p>
<h3>Datos de uso </h3>
<p>También podemos recopilar información sobre cómo se accede y utiliza el Servicio ("Datos de uso"). 
Estos Datos de uso pueden incluir información como la dirección del Protocolo de Internet de su computadora (por ejemplo, la dirección IP), el tipo de navegador, la versión del navegador, las páginas de nuestro sitio web que visita, la hora y la fecha de su visita, el tiempo dedicado a esas páginas,
único identificadores de dispositivos y otros datos de diagnóstico. </p>


